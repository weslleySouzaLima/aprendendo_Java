package src.poo.classeERecordEEncapsulamento;

public class DiferencaClasseVsRecord {
    /*
========================================
CLASSE vs RECORD
========================================

CLASSE:
- Pode ser mutável (dados podem mudar)
- Precisa escrever getters, setters, construtor
- Mais flexível

RECORD:
- É imutável (final)
- Menos código (automático)
- Ideal para DTOs (transferência de dados)

----------------------------------------
EXEMPLO REAL NO BACKEND:
----------------------------------------

Classe:
-> Entidade que pode mudar (ex: Usuario que atualiza senha)

Record:
-> Dados de entrada/saída (ex: UsuarioDTO)
*/

}
